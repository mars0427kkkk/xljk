package com.ruoyi.xljk.domain;

public class positiveNature {
    private Long A;
    private Long B;
    private Long C;
    private Long D;

    public Long getA() {
        return A;
    }

    public void setA(Long a) {
        A = a;
    }

    public Long getB() {
        return B;
    }

    public void setB(Long b) {
        B = b;
    }

    public Long getC() {
        return C;
    }

    public void setC(Long c) {
        C = c;
    }

    public Long getD() {
        return D;
    }

    public void setD(Long d) {
        D = d;
    }

    @Override
    public String toString() {
        return "positiveNature{" +
                "A=" + A +
                ", B=" + B +
                ", C=" + C +
                ", D=" + D +
                '}';
    }
}
